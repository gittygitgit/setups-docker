Faq
Why Docker Toolbox?
Installs docker, docker-compose and docker-machine.

Docker and docker-compose talk to a VM in Virtualbox via docker-machine.

---
Why not Docker for Mac?
Supports multiple VMs

---
How different from Docker for Mac?
Runs Linux images on a Virtualbox hypervixor.

---
How do I know if Docker Toolbox is already installed?
See if docker-machine is installed...

  Michaels-Air:docker grudkowm$ docker-machine -v
  docker-machine version 0.6.0, build e27fb87

